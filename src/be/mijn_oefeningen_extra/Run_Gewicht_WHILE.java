public class Run_Gewicht_WHILE {

    public static void main(String[] args) {
        int gewicht = 60;
        while (gewicht > 50){
            System.out.println("Je bent 1 kg vermagerd. Uw gewicht is " + gewicht + " Doe zo voort!");
            gewicht --;}
        {
            gewicht = 50;
            System.out.println(" Je hebt je streefgewicht van " +gewicht+" bereikt. Proficiat!");
        }



    }
}

